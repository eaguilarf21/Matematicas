#luego de que el usuario ingrese la altura y la base de un triangulo, calcular el area
#calcular el area de un triangulo teniendo su altura y su base.

#entrada
print("ingrese la base del triangulo")
base = float(input())
print("ingrese la altura del triangulo")
altura = float(input())

#proceso
area = base*altura/2

#salida
print("el area del triangulo es:", area)