#leer un numero entero comprendido entre 1 y 7 e imprimir en
# pantalla los dias de semana

#
print("ingrese el numero entre 1 y 7")
num = int(input())

#
if num ==1:
    print("lunes")
elif num==2:
    print("martes")
elif num==3:
    print("miercoles")
elif num==4:
    print("jueves")    
elif num==5:
    print("viernes")    
elif num==6:
    print("sabado")
elif num==7:
    print("domingo")    
else:
    print("")

